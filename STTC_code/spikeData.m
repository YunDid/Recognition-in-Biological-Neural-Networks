for i=1:length(fileNames)
    path=strcat(maindir,fileNames(i));
    path2=strcat(maindir2,fileNames2(i));
    data=load(path{1,1});data2=load(path2{1,1});
    name=fieldnames(data);
    a=[data.spike_indexes_seg0;data.spike_labels_seg0];
    spike=cell(length(data.unit_ids),1);
    unit_ids=data.unit_ids;
    unit_locations=data2.unit_locations';
    unit_electrode=zeros(length(unit_locations(1,:)),1);
    for k=1:length(data.unit_ids)
        ids=data.unit_ids(k);
        y=a(1,a(2,:)==ids);
        spike{k,1}=y;
    end
    for j=1:length(spike)
        spike{j,1}=round(spike{j,1}/25);
        spike{j,1}(spike{j,1}>300000)=[];%只采用前5分钟的数据分析
        spike{j,1}=double(spike{j,1})/1000;
    end
%% 将unit对应electrode
    for j=1:length(unit_locations(1,:))
        x=unit_locations(1,j);
        y=unit_locations(2,j);
        electrode=find(electrodes_location(1, :) == x & electrodes_location(2, :) == y);
        unit_electrode(j,1)=electrode;
    end
%% 64个电极spike
    spikes=cell(64,1);
    for k=1:64
        a=find(unit_electrode==k);
        if ~isempty (a)
            ap=[];
            for l=1:length(a)
                b=spike{a(l),1};
                ap=[ap b];ap=unique(ap);
                ap=sort(ap); spikes{k,1}=ap;
            end
        else
        end
    end
